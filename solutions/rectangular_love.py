import unittest


def find_rectangular_overlap(rect1, rect2):
    # Calculate the overlap between the two rectangles
    if rect1['left_x'] >= rect2['left_x'] + rect2['width'] or rect1['left_x'] + rect1['width'] <= rect2['left_x'] \
            or rect1['bottom_y'] >= rect2['bottom_y'] + rect2['height'] or rect1['bottom_y'] + rect1['height'] <= rect2[
        'bottom_y']:
        return {'left_x': None, 'bottom_y': None, 'width': None, 'height': None}

    right = max(rect1['left_x'] + rect1['width'], rect2['left_x'] + rect2['width'])
    left = min(rect1['left_x'], rect2['left_x'])
    width = rect1['width'] + rect2['width'] - (right - left)

    top = max(rect1['bottom_y'] + rect1['height'], rect2['bottom_y'] + rect2['height'])
    bottom = min(rect1['bottom_y'], rect2['bottom_y'])
    height = rect1['height'] + rect2['height'] - (top - bottom)

    if rect1['left_x'] < rect2['left_x']:
        left_x = rect2['left_x']
    else:
        left_x = rect1['left_x']

    if rect1['bottom_y'] > rect2['bottom_y']:
        bottom_y = rect1['bottom_y']
    else:
        bottom_y = rect2['bottom_y']

    return {'left_x': left_x, 'bottom_y': bottom_y, 'width': width, 'height': height}


# Tests

class Test(unittest.TestCase):

    def test_overlap_along_both_axes(self):
        rect1 = {
            'left_x': 1,
            'bottom_y': 1,
            'width': 6,
            'height': 3,
        }
        rect2 = {
            'left_x': 5,
            'bottom_y': 2,
            'width': 3,
            'height': 6,
        }
        expected = {
            'left_x': 5,
            'bottom_y': 2,
            'width': 2,
            'height': 2,
        }
        actual = find_rectangular_overlap(rect1, rect2)
        self.assertEqual(actual, expected)

